local falling = {}

function getElementsInRange(element,range,who)
	local x,y,z = getElementPosition(element)
	local col = createColSphere(x,y,z,range)
	local range = getElementsWithinColShape(col,who)
	destroyElement(col)
	return range
end

function zakonczScinke(hit,object)
	if not object then return end
	local x,y,z = getElementPosition(object)
	for i,v in pairs(getElementsInRange(hit,50,'player')) do
		triggerClientEvent(v,'playSoundDrzewa',resourceRoot,'sounds/Fall_down.mp3',{getElementPosition(hit)},50)
	end
	local rotation = {getElementRotation(hit)}
	setElementRotation(object,0,0,rotation[3]+180)
	moveObject(object,4000,x,y,z,90,0,_,"OutBounce")
	setElementCollisionsEnabled(object,false)
	falling[object] = true
	
	setTimer(function(object)
		for i=0,math.random(1,2) do
			local x,y,z = getElementPosition(object)
			--y = y - (i-0.5)*3
			z = z + 0.975
			local item,itemString = exports.DayZ:getItemTablePosition("Wood Pile")
			exports.DayZ:createItemPickup(item,x+math.random(-1,1),y+math.random(-1,1),z,itemString,1,nil,false)
		end
		destroyElement(object)
		falling[object] = nil
		setTimer(function(pos)
			spawnDrzewo(pos)
		end,60000*15,1,{x,y,z})
	end,4300,1,object)
end


local tree_editing = {}

local cooldown = {}

function atakuj(player)
	if getPedWeapon(player)==9 or getPedWeapon(player)==8 then
		if (cooldown[player] or 0) > getTickCount() then return end
		local col = createColSphere(Vector3(getElementPosition(player)),20)
		for i,v in pairs(getElementsWithinColShape(col,'player')) do
			triggerClientEvent(v,'playSoundDrzewa',resourceRoot,'sounds/hit.mp3',{getElementPosition(player)},20)
		end
		destroyElement(col)
		cooldown[player] = getTickCount() + 500
		local zdrowko = getElementData(tree_editing[player],'Drzewo-Zdrowie') or 0.2
		if zdrowko-0.03<=0 then
			local col = getElementChildren(tree_editing[player],'colshape')[1]
			for i,v in pairs(getElementsWithinColShape(col,'player')) do
				triggerClientEvent(v,'ShowMeRender',resourceRoot,nil,false)
				unbindKey(v,'mouse1','down',atakuj)
			end
			destroyElement(col)
			zakonczScinke(player,tree_editing[player])
			tree_editing[player] = false
			return
		end
		setElementData(tree_editing[player],'Drzewo-Zdrowie',zdrowko-0.03)
	end
end

addEventHandler('onColShapeHit',resourceRoot,function(hit)
	if hit and getElementType(hit)~='player' then return end
	if tree_editing[hit] then return end
	if falling[getElementParent(source)] then return end
	triggerClientEvent(hit,'ShowMeRender',resourceRoot,getElementParent(source),true)
	tree_editing[hit] = getElementParent(source)
	bindKey(hit,'mouse1','down',atakuj)
end)

addEventHandler('onColShapeLeave',resourceRoot,function(hit)
	if hit and getElementType(hit)~='player' then return end
	triggerClientEvent(hit,'ShowMeRender',resourceRoot,nil,false)
	tree_editing[hit] = nil
	unbindKey(hit,'mouse1','down',atakuj)
end)








