        for g,z in ipairs(ModelsTable[k]) do

            local txd = engineLoadTXD(k..tostring(z)..".txd", z);
            engineImportTXD(txd, z);
            local dff = engineLoadDFF(k..tostring(z)..".dff", z);
            engineReplaceModel(dff, z);

            for h, x in ipairs(colsTable) do
                if z == x then
                    col = engineLoadCOL( k..tostring(x)..".col", x )
                    engineReplaceCOL( col, x )
