

function applyMods(res)
	if (res==getThisResource()) then

		local skin = engineLoadTXD ( "zombies/22.txd" )
		engineImportTXD ( skin, 22 )
                
                local skin = engineLoadDFF ( "zombies/22.dff", 22 )
                engineReplaceModel ( skin, 22 )
	end
end
addEventHandler("onClientResourceStart", getRootElement(), applyMods)

